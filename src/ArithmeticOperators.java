/**
 * In this class we create all the arithmetic operations
 * @author Mirko Franco
 */

public class ArithmeticOperators {


    /**
     * Here we create the sum of 2 values
     * @param num1 first value
     * @param num2 second value
     */
    public void sum(int num1, int num2) {
        System.out.println(num1 + num2);
    }
    /**
     * Here we create the subtraction of 2 values
     * @param num1 first value
     * @param num2 second value
     */
    public void sub(int num1, int num2) {
        System.out.println(num1 - num2);
    }
    /**
     * Here we create the multiple of 2 values
     * @param num1 first value
     * @param num2 second value
     */
    public void mul(int num1, int num2) {
        System.out.println(num1 * num2);
    }
    /**
     * Here we create the division of 2 values
     * @param num1 first value
     * @param num2 second value
     */
    public void div(int num1, int num2) {
        System.out.println(num1 / num2);
    }


}
